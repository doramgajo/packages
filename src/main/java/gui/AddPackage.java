    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try {
            if (campoDescripcion.getText().isBlank() 
                    || campoDestino.getText().isBlank()
                    || campoDireccionDestinatario.getText().isBlank()) {
                JOptionPane.showMessageDialog(null, "Por favor, rellene todos los campos correctamente.");
            } else {
                ejecutarAnadido();
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(AddTruck.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void ejecutarAnadido() throws SQLException {
        CrudPackages.crear(new Package(campoDescripcion.getText(), 
                campoDestino.getText(), campoDireccionDestinatario.getText(), 
                Integer.parseInt(comboApartadoPostal.getSelectedItem().toString().substring(0, 
                        comboApartadoPostal.getSelectedItem().toString().indexOf(" "))), 
                comboRFCCamionero.getSelectedItem().toString().substring(0, 
                        comboRFCCamionero.getSelectedItem().toString().indexOf(" "))));
                JOptionPane.showMessageDialog(null, "Datos editados correctamente.");
                app.actualizarTablas();
                dispose();
    }
    
    
