    public void fijarDatos(Package paquete) throws SQLException {
        
        campoCodigo.setText(String.valueOf(paquete.getCodigopaquete()));
        antiguoCodigo = String.valueOf(paquete.getCodigopaquete());
        campoDescripcion.setText(paquete.getDescripcion());
        
        campoDestino.setText(String.valueOf(paquete.getDestino()));
        
        campoDireccionDestinatario.setText(paquete.getDirecciondestinatario());
        
        
        
        
        //Rellenar combo boxes con apartados y rfcs
        
        ArrayList<City> listaCiudades = CrudCities.leerTodos();
        
        String[] listaApartadosPostales = new String[listaCiudades.size()];
        
        for (int i = 0; i < listaApartadosPostales.length; i++) {
            listaApartadosPostales[i] = String.valueOf(listaCiudades.get(i)
                    .getApartadopostal() + " (" + listaCiudades.get(i).getNombre() + ")");
        }
        
        comboApartadoPostal.setModel(new javax.swing.DefaultComboBoxModel<>(listaApartadosPostales));
        
        ///////////////////////////////////////
        
         ArrayList<Trucker> listaCamioneros = CrudTruckers.leerTodos();
        
        String[] listaRFC = new String[listaCamioneros.size()];
        
        for (int i = 0; i < listaRFC.length; i++) {
            listaRFC[i] = String.valueOf(listaCamioneros.get(i).getRfc() + " (" 
                    + listaCamioneros.get(i).getNombre() + ")");
        }
        
        comboRFCCamionero.setModel(new javax.swing.DefaultComboBoxModel<>(listaRFC));

        
    }

