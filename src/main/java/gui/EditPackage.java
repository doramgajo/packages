    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try {
            if (campoCodigo.getText().isBlank() || campoDescripcion.getText().isBlank() 
                    || campoDestino.getText().isBlank()
                    || campoDireccionDestinatario.getText().isBlank()) {
                JOptionPane.showMessageDialog(null, "Por favor, rellene todos los campos correctamente.");
            } else if (!campoCodigo.getText().toUpperCase().matches("\\d{1,6}")) {
                JOptionPane.showMessageDialog(null, "Inserte un código correcto (Numérico)");
            } else if (CrudPackages.comprobarExistencia(campoCodigo.getText())) {
                
                if (campoCodigo.getText().equals(antiguoCodigo)) {
                    ejecutarEdicion();
                } else {
                    JOptionPane.showMessageDialog(null, "Ya existe un paquete con el código '" 
                            + campoCodigo.getText() +"'.");
                }
                
            } else {
                ejecutarEdicion();
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(AddTruck.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void ejecutarEdicion() throws SQLException {
        CrudPackages.actualizar(new Package(Integer.parseInt(
                campoCodigo.getText()), campoDescripcion.getText(), 
                campoDestino.getText(), campoDireccionDestinatario.getText(), 
                Integer.parseInt(comboApartadoPostal.getSelectedItem().toString().substring(0, 
                        comboApartadoPostal.getSelectedItem().toString().indexOf(" "))), 
                comboRFCCamionero.getSelectedItem().toString().substring(0, 
                        comboRFCCamionero.getSelectedItem().toString().indexOf(" "))), 
                antiguoCodigo);
                JOptionPane.showMessageDialog(null, "Datos editados correctamente.");
                app.actualizarTablas();
                dispose();
    }
    
    
