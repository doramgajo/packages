    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        System.out.println(ConnectionController.getConector());
        try {
            if (campoFecha.getText().isBlank()) {
                JOptionPane.showMessageDialog(null, "Por favor, rellene todos los campos correctamente.");
            } else if (!ConnectionController.getConector().equals("h2") && !campoFecha.getText().matches("\\d{1,2}/\\d{1,2}/\\d{4}")) {
                JOptionPane.showMessageDialog(null, "Inserte una fecha correcta (Formato DD/MM/AAAA)");
            } else if (ConnectionController.getConector().equals("h2") && !campoFecha.getText().matches("\\d{4}-\\d{2}-\\d{2}")) {
                JOptionPane.showMessageDialog(null, "Inserte una fecha correcta (Formato AAAA-MM-DD)");
            } else if (CrudAssignment.comprobarExistencia(comboPlaca.getSelectedItem().toString().substring(0, 
                        comboPlaca.getSelectedItem().toString().indexOf(" ")), 
                    comboRFC.getSelectedItem().toString().substring(0, 
                            comboRFC.getSelectedItem().toString().indexOf(" ")), 
                    campoFecha.getText())) {
                
                    JOptionPane.showMessageDialog(null, "Ya existe una asignación con los datos introducidos.");
                
                
            } else {
                ejecutarAnadido();
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(AddTruck.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void ejecutarAnadido() throws SQLException {
        CrudAssignment.crear(new Assignment(comboRFC.getSelectedItem().toString().substring(0, 
                            comboRFC.getSelectedItem().toString().indexOf(" ")),
                comboPlaca.getSelectedItem().toString().substring(0, 
                        comboPlaca.getSelectedItem().toString().indexOf(" ")),
        campoFecha.getText()));
                JOptionPane.showMessageDialog(null, "Datos editados correctamente.");
                app.actualizarTablas();
                dispose();
    }
    
    
