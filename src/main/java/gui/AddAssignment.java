public void fijarDatos() throws SQLException {
               
        ArrayList<Truck> listaCamiones = CrudTrucks.leerTodos();
        
        String[] listaPlacas = new String[listaCamiones.size()];
        
        for (int i = 0; i < listaPlacas.length; i++) {
            listaPlacas[i] = String.valueOf(listaCamiones.get(i)
                    .getPlaca()+ " (" + listaCamiones.get(i).getModelo()+ ")");
        }
        
        comboPlaca.setModel(new javax.swing.DefaultComboBoxModel<>(listaPlacas));
        
        
        ///////////////////////////////////////
        
         ArrayList<Trucker> listaCamioneros = CrudTruckers.leerTodos();
        
        String[] listaRFC = new String[listaCamioneros.size()];
        
        for (int i = 0; i < listaRFC.length; i++) {
            listaRFC[i] = String.valueOf(listaCamioneros.get(i).getRfc() + " (" 
                    + listaCamioneros.get(i).getNombre() + ")");
        }
        
        comboRFC.setModel(new javax.swing.DefaultComboBoxModel<>(listaRFC));

    }
   

