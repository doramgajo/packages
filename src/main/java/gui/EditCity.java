    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (campoApartadoPostal.getText().isBlank() || aprtadoNombre.getText().isBlank()) {
                JOptionPane.showMessageDialog(null, "Por favor, rellene todos los campos correctamente.");
            } else if (!campoApartadoPostal.getText().matches("[1-9]\\d{1,4}")) {
                JOptionPane.showMessageDialog(null, "Inserte un apartado postal correcto (2 a 3 d√≠gitos).");
            } else if (CrudCities.comprobarExistencia(campoApartadoPostal.getText())) {
                  if (campoApartadoPostal.getText().equals(antiguoApartadoPostal)) {
                    ejecutarEdicion();
                } else {
                    JOptionPane.showMessageDialog(null, "Ya existe una ciudad con apartado postal '" 
                            + campoApartadoPostal.getText() +"'.");
                }
            } else {      
                ejecutarEdicion();
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(AddTruck.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void ejecutarEdicion() throws SQLException {
        City ciudad = new City(Integer.parseInt(campoApartadoPostal.getText()), aprtadoNombre.getText());  
                  CrudCities.actualizar(ciudad, antiguoApartadoPostal);
                JOptionPane.showMessageDialog(null, "Datos editados correctamente.");
                app.actualizarTablas();
                dispose();
    }
    
    
